public class Guitar{

    public String Bass() {

        return "I am a bass guitar!";

    }

    public String Bass(int string, String sound) {

        return "The bass guitar has " + string + " strings and makes " + sound + " sounds!";

    }

    public String Bass(int string, String sound, int size) {

        return "Well my bass guitar has " + string + " strings, makes " + sound + ", and is about " + size + " inches!";

    }


}