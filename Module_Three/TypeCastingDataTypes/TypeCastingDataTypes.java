class TypeCastingDataTypes {
  
  public static void main(String[] args) {

    float deposit = 6.50f;

    int balance = 0;

    int z = 10;

    float y = 3.755f;

    balance = balance + (int) deposit;

    System.out.println("Mary started with: " + deposit + ". \n This is the amount the bank registered: " + balance);
    
    System.out.println(z + y);
    
  }
}
