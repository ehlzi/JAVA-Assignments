class Chef {

    static void cookFood(int dish){

        switch (dish) {

            case 1: System.out.println("Here are your fries");

                break;

            case 2: System.out.println("Here is your steak");

                break;

            case 3: System.out.println("Here are your wings");

                break;

            case 4: System.out.println("Here is your smoothie");

                break;

            case 5: System.out.println("Here is your salad");

                break;

            default: System.out.println("You want nothing?!");

                break;

        }

    }

}